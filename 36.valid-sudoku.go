func isValidSudoku(board [][]byte) bool {
	var rows [9][9]int
	var cols [9][9]int
	var box [3][3][9]int

	for i := 0; i < 9; i++ {
		for j := 0; j < 9; j++ {
			if board[i][j] == '.' {
				continue
			}
			num := board[i][j] - '0' - 1
			rows[i][num]++
			cols[j][num]++
			box[i/3][j/3][num]++
			if rows[i][num] > 1 || cols[j][num] > 1 || box[i/3][j/3][num] > 1 {
				return false
			}
		}
	}
	return true
}

